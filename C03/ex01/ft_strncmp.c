int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	if (n == 0)
		return (0);
	while ((s1[i] || s2[i]) && n - 1 != 0)
	{
		if (s1[i] > s2[i] || s1[i] < s2[i])
		{
			a = s1[i] - s2[i];
			return (a);
		}
		i++;
		n--;
	}
	return (s1[i] - s2[i]);
}
