int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	a;

	a = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			a++;
		}
		i++;
	}
	if (a == i)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
