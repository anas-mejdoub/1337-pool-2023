int	ft_str_is_alpha(char *str)
{
	int	i;
	int	a;
	int	j;

	j = 0;
	a = 0;
	i = 0;
	while (str[i])
	{
		if ((str[i] < 'A' || str[i] > 'Z') && (str[i] < 'a' || str[i] > 'z'))
		{
			a++;
		}
		i++;
	}
	if (a == 0)
	{
		j = 1;
	}
	else if (a >= 1)
	{
		j = 0;
	}
	return (j);
}
