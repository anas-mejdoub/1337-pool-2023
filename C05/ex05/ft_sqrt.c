int	ft_sqrt(int nb)
{
	int	i;
	int	a;

	i = 1;
	a = 1;
	while (a)
	{
		if (i > 46340)
		{
			return (0);
		}
		else if ((i * i) == nb)
		{
			a = 0;
			return (i);
		}
		i++;
	}
	return (0);
}
